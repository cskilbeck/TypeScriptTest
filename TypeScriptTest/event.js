chs.Event = (function () {
    "use strict";

    //////////////////////////////////////////////////////////////////////

    var Event = function (type) {
        this.type = type;
    };

    //////////////////////////////////////////////////////////////////////

    Event.leftMouseDown = 1;
    Event.leftMouseUp = 2;
    Event.rightMouseDown = 3;
    Event.rightMouseUp = 4;
    Event.mouseMove = 5;

    return Event;

}());

//////////////////////////////////////////////////////////////////////

chs.MouseEvent = (function () {
    "use strict";

    //////////////////////////////////////////////////////////////////////

    var MouseEvent = function (type, pos) {
        chs.Event.call(this, type);
        this.position = pos;
    };

    //////////////////////////////////////////////////////////////////////

    Object.defineProperty(MouseEvent, "x", {
        get: function () {
            return this.position.x;
        }
    });

    //////////////////////////////////////////////////////////////////////

    Object.defineProperty(MouseEvent, "y", {
        get: function () {
            return this.position.y;
        }
    });

    //////////////////////////////////////////////////////////////////////

    chs.extend(MouseEvent, chs.Event);

    return MouseEvent;

}());

